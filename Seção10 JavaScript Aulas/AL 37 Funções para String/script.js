var a = 'CAIO CASTRO';

var text = a.toLowerCase();
document.write(text)
document.writeln('<br>')

var text = a.toUpperCase();
document.write(text)
document.writeln('<br>')

var text = a.charAt(2);
document.write(text)
document.writeln('<br>')

var text = a.trim(2);
document.write(text)
document.writeln('<br>')

var text = a.concat();
document.write(text)
document.writeln('<br>')

var text = a.replace();
document.write(text)
document.writeln('<br>')